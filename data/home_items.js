// ฟังก์ชันสำหรับสร้าง HTML ของรายการสินค้า
function generateItemHTML(item) {
  return `
    <li>
      <a href="${item.link}" title="${item.title}" target="_blank">
        <div class="img">
          <img width="60" height="60"
            src="${item.image}"
            alt="${item.title}" />
        </div>
        <div class="T">
          <div class="name">${item.title}</div>
          <div class="info">
            <span>${item.region}</span>
          </div>
        </div>
      </a>
    </li>
  `;
}

// ฟังก์ชันสำหรับสร้าง HTML ของหมวดหมู่
function generateCategoryHTML(category) {
  return `
    <div class="category swiper-slide" role="group">
      <div class="title">
        <h2>${category.title}</h2>
        <a href="${category.moreLink}" class="btw more">
          <span>ดูเพิ่มเติม</span>
          <i class="fas fa-chevron-right"></i>
        </a>
      </div>
      <div class="ItemList">
        ${category.items.map(item => generateItemHTML(item)).join('')}
      </div>
    </div>
  `;
}

// ฟังก์ชันสำหรับสร้าง HTML ของ Home Items ทั้งหมด
function generateHomeItemsHTML() {
  return homeItemsData.categories.map(category => generateCategoryHTML(category)).join('');
}

